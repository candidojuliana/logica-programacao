// Pedir para usuário digitar dois números, depois mostrar na tela a subtração, multiplicação e divisão desses dois números

var n1 = prompt("Digite um número");
   n1 = parseInt(n1);

var n2 = prompt("Digite outro número");
   n2 = parseInt(n2);

alert( "a subtração é: "+ (n1 - n2));

alert( "a multiplicação é: "+ (n1 * n2));

alert( "a divisão é: "+ (n1 / n2));